def _sep_entry(**overrides):
    base = {
        "task_status": "Open",
        "country": "US",
        "task_type": "Respond to Non-Final Office Action - 3 Month Deadline",
        "sep_status": "5G NR",
        "tier": None,
    }
    base.update(overrides)
    return base


class TestUsNonFinalRule:
    def test_matches_and_generates_tasks(self):
        entry = _us_nonfinal_entry()
        tasks = generate_tasks_for_entry(entry)
        names = [t["name"] for t in tasks]
        assert any("Review Office Action" in n for n in names)
        assert any("FILE RESPONSE" in n for n in names)

    def test_tasks_sorted_by_offset_descending(self):
        entry = _us_nonfinal_entry()
        tasks = generate_tasks_for_entry(entry)
        offsets = [t["offset_days"] for t in tasks]
        assert offsets == sorted(offsets, reverse=True)

    def test_closed_entry_produces_no_tasks(self):
        entry = _us_nonfinal_entry(task_status="Closed")
        tasks = generate_tasks_for_entry(entry)
        # No rule should fire for a closed entry
        assert tasks == []

    def test_wrong_country_produces_no_nonfinal_tasks(self):
        # EP entry should not match us_nonfinal_oa_response
        entry = _us_nonfinal_entry(country="EP")
        tasks = generate_tasks_for_entry(entry)
        nonfinal_task_names = [
            t["name"] for t in tasks if "Review Office Action" in t["name"]
            and "foreign" not in t["name"].lower()
        ]
        # The us_nonfinal rule should NOT fire; the foreign rule may fire
        us_rule_tasks = [t for t in tasks if t.get("help_key") == "review_office_action"
                         and t["offset_days"] == 75]
        assert not us_rule_tasks


